start() :-
	nl,
	write('Enter a symptom \n'),
	read(Y),
	findall(X, symptom(X, Y) , Z),
	write('list of possibilities: \n'),
	print(Z),
	nl,
	searchAgain(Z).

searchAgain(Z) :-
	(length(Z, 0) -> exit();
	nl,
	write('Enter another symptom or type "1" to list treatments/tests for given possibilities  \n'),
	read(J),
	(J==1 -> printTreatment(Z), printTest(Z);
	findall(X, symptom(X, J) , Q),
	inter(Z, Q, M),
	write('list of possibilities: \n'),
	print(M),
	nl,
	searchAgain(M))).

printTreatment([]) :-
	nl.
printTreatment([X|Y]) :-
	findall(R, treatment(X, R) , Q),
	nl,
	write('Treatments for '),
	write(X),
	write(':'),
	print(Q),
	nl,
	printTreatment(Y).

printTest([]) :-
	nl,
	write('Thanks for using the medical diagnosis application \n').
